# Exemplo 2 - Variaveis
nome = "Pedro Oliveira"  # Cria a variavel nome e atribui o valor entreas aspas
idade = 21  # Cria a variavel idade e atribui 21
matriculado = True  # Cria a variavel matriculado e atribui verdadeiro
mensalidade = 79.9  # Cria a variavel mensalidade e atribui 79.9

print("A variavel nome tem o tipo", type(nome), " e o valor ", nome)
print("A variavel idade tem o tipo", type(idade), " e o valor ", idade)
print("A variavel matriculado tem o tipo", type(matriculado), " e o valor ", matriculado)
print("A variavel mensalidade tem o tipo", type(mensalidade), " e o valor ", mensalidade)
