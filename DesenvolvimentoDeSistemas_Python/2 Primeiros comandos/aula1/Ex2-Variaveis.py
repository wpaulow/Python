# Exemplo 2 - Variaveis

nome = "Pedro Oliveira" # Cria a variável nome e atribuí o valor entre as aspas
idade = 21 # Cria a variável idade e atribuí 21
matriculado = True # Cria a variável matriculado e atribuí o valor verdadeiro
mensalidade = 79.9 # Cria a variável mensalidade e atribuí o valor 79.9

print ("A variável nome tem o tipo", type(nome), " e o valor ", nome)
print ("A variável idade tem o tipo", type(idade), " e o valor ", idade)
print ("A variável matriculado tem o tipo", type(matriculado), " e o valor ", matriculado)
print ("A variável mensalidade tem o tipo", type(mensalidade), " e o valor ", mensalidade)
