# Exemplo 1 - Startswith e Endswith
nome = "João da Silva"  # cria a String
print(nome.startswith("João"))  # Testa se a String começa com João, nesse caso True
print(nome.startswith("joão"))  # Nesse caso da False por causa do j minusculo
print(nome.endswith("Silva"))  # Testa se a String termina com Silva,neste caso True
